namespace ExpressiveWeb.Designer.Extensions;

internal static class TypesExtensions
{
    internal static string ToJsFormat(this bool type)
    {
        return type ? "true" : "false";
    }
    
    internal static bool IsNumeric(this object value)
    {
        return value is sbyte
               || value is byte
               || value is short
               || value is ushort
               || value is int
               || value is uint
               || value is long
               || value is ulong
               || value is float
               || value is double
               || value is decimal;
    }
}